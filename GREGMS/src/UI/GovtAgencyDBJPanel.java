    private void populateConsumer() {
        
        DefaultTableModel model = (DefaultTableModel) jTable6.getModel();
    

        model.setRowCount(0);

        for (ConsumerTransaction ct : business.getGridData().getCt()) {
            Object[] row = new Object[2];
            row[0] = ct.getQuantityOfEnergy(); // Assuming License ID is UUID
            row[1] = ct.getPrice();
            model.addRow(row);
        }
        
        jTable6.setModel(model);
        ChartPanel chartPanel = ChartUtils.createEnergySourceChart(business.getGridData().getTc().getAllTransactions());
        jPanel7.removeAll();
        // Assuming 'this' is a JPanel, you add the chartPanel to it
        jPanel7.setLayout(new BorderLayout());
        jPanel7.add(chartPanel, BorderLayout.CENTER);

        // Revalidate and repaint to ensure the chart is displayed
        jPanel7.revalidate();
        jPanel7.repaint();
        
    }
