    public void populateAggregatedEnergyProducersTable(JTable table, EnergyProducerCatalog energyProducersCatalog) {
        List<EnergyProducer> energyProducers = energyProducersCatalog.getAllProducers();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); // Clear existing data

        for (EnergyProducer producer : energyProducers) {
            EnergyDataCatalog energyDataCatalog = producer.getEdc();

            // Create a map to store the aggregated data
            Map<String, Double> aggregatedData = new HashMap<>();

            // Aggregate data
            for (EnergyData data : energyDataCatalog.getEnergyDataList()) {
                String source = data.getSourceOfEnergy();
                double quantity = data.getEnergyOutput();
                aggregatedData.put(source, aggregatedData.getOrDefault(source, 0.0) + quantity);
            }

            // Add aggregated data to the table model
            for (Map.Entry<String, Double> entry : aggregatedData.entrySet()) {
                Object[] row = new Object[3];
                row[0] = producer.getName(); // Or any other identifier of the producer
                row[1] = entry.getKey(); // Source of energy
                row[2] = entry.getValue(); // Aggregated quantity produced
                model.addRow(row);
            }
        }
        
        ChartPanel chartPanel = ChartUtils.createEnergySourcePieChart(energyProducersCatalog);

        // Assuming 'this' is a JPanel, you add the chartPanel to it
        jPanel10.removeAll();
        jPanel10.setLayout(new BorderLayout());
        jPanel10.add(chartPanel, BorderLayout.CENTER);

        // Revalidate and repaint to ensure the chart is displayed
        jPanel10.revalidate();
        jPanel10.repaint();

        // Set the model to the table
        table.setModel(model);
    }
    
